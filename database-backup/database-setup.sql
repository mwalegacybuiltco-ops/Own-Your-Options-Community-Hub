create or replace function public.oyo_create_member_records()
returns trigger language plpgsql security definer set search_path = public
as $$
begin
  insert into public.oyo_member_profiles (user_id, display_name)
  values (new.id, coalesce(new.raw_user_meta_data ->> 'name', split_part(new.email, '@', 1)))
  on conflict (user_id) do nothing;
  insert into public.oyo_memberships (user_id, email)
  values (new.id, new.email)
  on conflict (user_id) do nothing;
  return new;
end;
$$;

drop trigger if exists oyo_on_auth_user_created on auth.users;
create trigger oyo_on_auth_user_created after insert on auth.users
for each row execute procedure public.oyo_create_member_records();

create or replace function public.oyo_set_member_access(
  target_user_id uuid,
  new_status text,
  reason text default ''
)
returns void language plpgsql security definer set search_path = public, auth
as $$
begin
  if not public.oyo_is_owner() then raise exception 'Owner access required'; end if;
  if exists (select 1 from public.oyo_app_owner where user_id = target_user_id) then
    raise exception 'The owner account cannot be blocked or archived';
  end if;
  if new_status not in ('Active','Blocked','Archived') then raise exception 'Invalid member status'; end if;
  update public.oyo_memberships
  set status = new_status,
      status_reason = reason,
      blocked_at = case when new_status = 'Blocked' then now() else null end,
      archived_at = case when new_status = 'Archived' then now() else null end
  where user_id = target_user_id;
  if new_status <> 'Active' then
    update public.oyo_member_profiles set directory_visible = false where user_id = target_user_id;
    update public.oyo_staff_roles set status = 'Suspended' where user_id = target_user_id;
  end if;
end;
$$;

create or replace function public.oyo_permanently_delete_member(target_user_id uuid)
returns void language plpgsql security definer set search_path = public, auth
as $$
begin
  if not public.oyo_is_owner() then raise exception 'Owner access required'; end if;
  if exists (select 1 from public.oyo_app_owner where user_id = target_user_id) then
    raise exception 'The owner account cannot be deleted';
  end if;
  if not exists (
    select 1 from public.oyo_memberships where user_id = target_user_id and status = 'Archived'
  ) then
    raise exception 'Archive the member before permanently deleting the account';
  end if;
  delete from auth.users where id = target_user_id;
end;
$$;

revoke all on function public.oyo_set_member_access(uuid,text,text) from public, anon, authenticated;
grant execute on function public.oyo_set_member_access(uuid,text,text) to authenticated;
revoke all on function public.oyo_permanently_delete_member(uuid) from public, anon, authenticated;
grant execute on function public.oyo_permanently_delete_member(uuid) to authenticated;

