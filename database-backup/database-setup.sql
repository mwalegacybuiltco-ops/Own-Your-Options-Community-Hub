create extension if not exists pgcrypto;

create table if not exists public.oyo_app_owner (
  singleton boolean primary key default true check (singleton),
  user_id uuid not null unique references auth.users(id) on delete restrict,
  assigned_at timestamptz default now()
);

create or replace function public.oyo_is_owner()
returns boolean language sql stable security definer set search_path = public
as $$
  select exists (
    select 1 from public.oyo_app_owner where user_id = auth.uid()
  );
$$;

create table if not exists public.oyo_staff_roles (
  user_id uuid primary key references auth.users(id) on delete cascade,
  role text not null default 'moderator' check (role in ('moderator')),
  status text not null default 'Active' check (status in ('Active','Suspended')),
  assigned_by uuid references auth.users(id),
  assigned_at timestamptz default now(),
  notes text default ''
);

create or replace function public.oyo_is_moderator()
returns boolean language sql stable security definer set search_path = public
as $$
  select exists (
    select 1 from public.oyo_staff_roles
    where user_id = auth.uid() and role = 'moderator' and status = 'Active'
  );
$$;

create or replace function public.oyo_can_moderate()
returns boolean language sql stable security definer set search_path = public
as $$ select public.oyo_is_owner() or public.oyo_is_moderator(); $$;

create table if not exists public.oyo_member_profiles (
  user_id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null default 'Own Your Options Member',
  province_state text default '',
  skills text[] default '{}',
  business_interests text[] default '{}',
  goals text[] default '{}',
  bio text default '',
  avatar_url text default '',
  directory_visible boolean default true,
  updated_at timestamptz default now()
);

create table if not exists public.oyo_memberships (
  user_id uuid primary key references auth.users(id) on delete cascade,
  email text not null,
  plan text not null default 'Collective',
  status text not null default 'Active',
  status_reason text default '',
  blocked_at timestamptz,
  archived_at timestamptz,
  joined_at timestamptz default now(),
  last_active_at timestamptz default now(),
  admin_notes text default ''
);

create or replace function public.oyo_has_hub_access()
returns boolean language sql stable security definer set search_path = public
as $$
  select public.oyo_is_owner() or exists (
    select 1 from public.oyo_memberships
    where user_id = auth.uid() and status = 'Active'
  );
$$;

create table if not exists public.oyo_hub_content (
  id bigint generated by default as identity primary key,
  area text not null,
  type text not null,
  title text not null,
  description text default '',
  body text default '',
  status text not null default 'Draft',
  file_path text default '',
  external_url text default '',
  sort_order integer default 0,
  created_by uuid references auth.users(id),
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.oyo_member_progress (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  content_id bigint not null references public.oyo_hub_content(id) on delete cascade,
  status text not null default 'Not started',
  percent integer not null default 0 check (percent between 0 and 100),
  reflection text default '',
  started_at timestamptz,
  completed_at timestamptz,
  updated_at timestamptz default now(),
  unique(user_id, content_id)
);

create table if not exists public.oyo_community_posts (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  space text not null,
  body text not null,
  image_path text default '',
  status text not null default 'Published',
  created_at timestamptz default now()
);

create table if not exists public.oyo_community_comments (
  id bigint generated by default as identity primary key,
  post_id bigint not null references public.oyo_community_posts(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  body text not null,
  status text not null default 'Published',
  created_at timestamptz default now()
);

create table if not exists public.oyo_event_registrations (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  content_id bigint not null references public.oyo_hub_content(id) on delete cascade,
  registered_at timestamptz default now(),
  attended boolean default false,
  unique(user_id, content_id)
);

